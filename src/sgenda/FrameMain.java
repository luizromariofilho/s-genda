    private void managerStatus() {
        switch (this.status) {
            case NEW:
                this.btnDelete.setEnabled(false);
                this.btnUpdate.setEnabled(false);
                break;
            case UPDATE:
                break;
            case DELETE:
                break;
        }
    }

    private void setStatus(Status status) {
        this.status = status;
        managerStatus();
    }

